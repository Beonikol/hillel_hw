public class Car {

    int id;
    String marck;
    String model;
    int year;
    String color;
    double price;
    String number;

    public Car(int id, String marck, String model, int year, String color, double price, String number) {
        this.id = id;
        this.marck = marck;
        this.model = model;
        this.year = year;
        this.color = color;
        this.price = price;
        this.number = number;
    }
}
